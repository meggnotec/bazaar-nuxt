<template>
  <div class="relative">
    <label for="sort-select" class="sr-only">Sort products</label>
    <div class="flex items-center gap-2">
      <span class="text-sm text-surface-500 dark:text-surface-400 hidden sm:inline">Sort by:</span>
      <div class="relative">
        <select
          id="sort-select"
          :value="modelValue"
          name="sort"
          class="appearance-none rounded-lg border border-surface-300 dark:border-surface-600 bg-white dark:bg-surface-800 ps-4 pe-10 py-2.5 text-sm text-surface-700 dark:text-surface-300 focus:border-primary-500 focus:outline-none focus:ring-2 focus:ring-primary-500/20 cursor-pointer transition-colors"
          @change="handleChange"
        >
          <option value="featured">Featured</option>
          <option value="price-asc">Price: Low to High</option>
          <option value="price-desc">Price: High to Low</option>
          <option value="newest">Newest</option>
          <option value="rating">Best Rated</option>
        </select>
        <!-- Custom chevron -->
        <div class="pointer-events-none absolute inset-y-0 end-0 flex items-center pe-3">
          <svg class="h-4 w-4 text-surface-400 dark:text-surface-500" fill="none" viewBox="0 0 24 24" stroke-width="2" stroke="currentColor" aria-hidden="true">
            <path stroke-linecap="round" stroke-linejoin="round" d="M8.25 15L12 18.75 15.75 15m-7.5-6L12 5.25 15.75 9" />
          </svg>
        </div>
      </div>
    </div>
  </div>
</template>

<script setup lang="ts">
defineProps<{
  modelValue: string;
}>();

const emit = defineEmits<{
  'update:modelValue': [value: string];
}>();

function handleChange(e: Event) {
  emit('update:modelValue', (e.target as HTMLSelectElement).value);
}
</script>
